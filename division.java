public class Division 
{
    public static void main(String[] args) 
    {
        // Define two numbers
        double number1 = 15.0;
        double number2 = 3.0;
        
        // Divide number1 by number2
        double result = number1 / number2;
        
        // Print the result
        System.out.println("Result of division: " + result);
    }
}
