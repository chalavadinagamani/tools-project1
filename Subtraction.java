public class Subtraction
 {
    public static void main(String[] args) 
{
        // Define two numbers
        int number1 = 20;
        int number2 = 10;
        
        // Subtract number2 from number1
        int result = number1 - number2;
        
        // Print the result
        System.out.println("Result of subtraction: " + result);
    }
}
